package exercise;
//	计算浮点类型的最大值和最小值
public class FloatDouble {
	public static void main(String[] args) {
//		float的最大值
		float float_max = Float.MAX_VALUE;
		System.out.println("float的最大值："+float_max);
//		float的最小值
		float float_min = Float.MIN_VALUE;
		System.out.println("float的最小值："+float_min);
//		double的最大值
		double double_max = Double.MAX_VALUE;
		System.out.println("double的最大值："+double_max);
//		double的最小值
		double double_min = Double.MIN_VALUE;
		System.out.println("double的最小值："+double_min);
	}
}
